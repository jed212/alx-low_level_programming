

/**
  * op_add - sum of two given integers, a and b.
  * @a: first integer
  * @b: second integer
  * Return: the sum of a and b.
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - substraction of two integers,a and b.
  * @a: first int
  * @b: second int
  * Return: result of substraction
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - multiplies two given integers, a and b.
  * @a: first int
  * @b: second int
  * Return: the product.
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - divides two integers,a and b.
  * @a: the dividend
  * @b: the divisor.
  * Return: returns the quotient.
  */ 
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - computes the remainder of the division of two integers.
  * @a: the dividend
  * @b: the divisor.
  * Return: returns the remainder.
  */
int op_mod(int a, int b)
{
	return (a % b);
}
